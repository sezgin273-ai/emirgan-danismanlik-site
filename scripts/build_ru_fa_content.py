def build_lang(tr: dict, trans: dict, lang_code: str) -> dict:
    data = copy.deepcopy(tr)
    overlay(data, trans)
    data["site"]["lang"] = lang_code
    apply_lang_independent(data, tr)
    return data


def main() -> None:
    tr = json.loads(TR_PATH.read_text(encoding="utf-8"))
    for lang_code, trans, filename in (
        ("ru", RU, "content.ru.json"),
        ("fa", FA, "content.fa.json"),
    ):
        data = build_lang(tr, trans, lang_code)
        path = ROOT / "content" / filename
        path.write_text(
            json.dumps(data, indent=4, ensure_ascii=False) + "\n",
            encoding="utf-8",
        )
        print(f"Wrote {path}")


if __name__ == "__main__":
    main()
