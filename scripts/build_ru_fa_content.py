def overlay(target: dict, trans: dict) -> None:
    for key, value in trans.items():
        if isinstance(value, dict) and key in target and isinstance(target[key], dict):
            if all(not isinstance(v, (dict, list)) for v in value.values()):
                target[key].update(value)
            else:
                overlay(target[key], value)
        elif isinstance(value, list) and key in target and isinstance(target[key], list):
            for i, item in enumerate(value):
                if isinstance(item, dict) and i < len(target[key]):
                    if all(not isinstance(v, (dict, list)) for v in item.values()):
                        target[key][i].update(item)
                    else:
                        overlay(target[key][i], item)
                else:
                    target[key][i] = item
        else:
            target[key] = value


